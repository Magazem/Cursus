#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	n;
	int	r;

	i = 0;
	n = 1;
	r = 0;
	while (str[i] == ' ' || (str[i] > '\b' && str[i] <= '\r'))
		i++;
	if (str[i] == '-')
		n = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = (r * 10 + (str[i] - 48));
		i++;
	}
	return (n * r);
}
